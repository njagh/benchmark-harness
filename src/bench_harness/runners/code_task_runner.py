"""Code task runner — executes generated code and runs unit tests."""

from __future__ import annotations

import ast
import logging
import os
import re
import subprocess
import sys
import tempfile
import textwrap
from typing import Any

logger = logging.getLogger(__name__)


def _parse_pytest_xml(stdout: str) -> dict[str, Any]:
    """Parse pytest terminal output to extract test results.

    Returns a dict with keys: passed, failed, skipped, total, items.
    Each item in 'items' is a dict with name, outcome, longrepr.
    """
    result = {
        "passed": 0,
        "failed": 0,
        "skipped": 0,
        "total": 0,
        "items": [],
    }

    for line in stdout.splitlines():
        line = line.strip()
        item_match = re.match(
            r"^(\S+)\s+(PASSED|FAILED|SKIPPED|ERROR)(?:\s+(.*))?$", line
        )
        if item_match:
            test_name = item_match.group(1)
            outcome = item_match.group(2)
            result["total"] += 1

            if outcome == "PASSED":
                result["passed"] += 1
                result["items"].append({"name": test_name, "outcome": "passed"})
            elif outcome == "FAILED" or outcome == "ERROR":
                result["failed"] += 1
                result["items"].append(
                    {"name": test_name, "outcome": "failed"}
                )
            elif outcome == "SKIPPED":
                result["skipped"] += 1
                result["items"].append(
                    {"name": test_name, "outcome": "skipped"}
                )

    return result


def _check_syntax(code: str, filename: str = "<generated>") -> tuple[bool, str | None]:
    """Check if code is syntactically valid Python.

    Returns (is_valid, error_message).
    """
    try:
        ast.parse(textwrap.dedent(code))
        return True, None
    except SyntaxError as e:
        return False, str(e)


def _attempt_import(code: str, module_name: str = "generated_code") -> tuple[bool, str | None]:
    """Attempt to import the generated code as a Python module.

    Returns (is_importable, error_message).
    """
    try:
        import importlib.util
        spec = importlib.util.spec_from_loader(
            module_name, loader=None, origin="<in-memory>"
        )
        if spec is None:
            return False, "Could not create module spec"
        module = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(module)  # type: ignore[union-attr]
        return True, None
    except (ImportError, ModuleNotFoundError, AttributeError) as e:
        return False, str(e)
    except Exception as e:
        return False, str(e)


def _detect_function_signature(code: str) -> str | None:
    """Extract function signatures from generated code.

    Returns a string of all function definitions found, or None.
    """
    try:
        tree = ast.parse(textwrap.dedent(code))
    except SyntaxError:
        return None

    signatures = []
    for node in ast.walk(tree):
        if isinstance(node, ast.FunctionDef) or isinstance(node, ast.AsyncFunctionDef):
            args = []
            for arg in node.args.args:
                args.append(arg.arg)
            sig = f"def {node.name}({', '.join(args)})"
            signatures.append(sig)

    return "\n".join(signatures) if signatures else None


class CodeTaskRunner:
    """Runs code generation tasks by executing generated code and running tests.

    Supports two task types:
    - function_completion: model generates a Python function
    - patch_generation: model generates a unified diff to apply

    Usage:
        runner = CodeTaskRunner()
        result = runner.run(task, generated_code, params)
    """

    def __init__(self, preserve_on_success: bool = False):
        self._preserve_on_success = preserve_on_success

    def run(
        self,
        task: dict[str, Any],
        generated_code: str,
        params: dict[str, Any] | None = None,
    ) -> dict[str, Any]:
        """Run a code generation task.

        Args:
            task: Task dict with expected, scoring, and code_type fields.
            generated_code: The code generated by the model.
            params: Optional run parameters.

        Returns:
            Dict with score_primary, tests_passed, tests_failed, tests_total,
            test_output, exit_code, generated_code, code_status.
        """
        params = params or {}
        suite_id = params.get("suite_id", "")
        task_id = task.get("id", "unknown")
        expected = task.get("expected", {})
        test_code = expected.get("test_code", "")
        entry_point = expected.get("entry_point")
        test_framework = expected.get("test_framework", "pytest")
        test_files = expected.get("test_files") or []
        code_type = task.get("code_type", "function_completion")

        run_dir = tempfile.mkdtemp(prefix="code_task_")
        logger.info("Created temp directory %s for task %s", run_dir, task_id)

        try:
            if code_type == "patch_generation":
                return self._run_patch_task(
                    run_dir=run_dir,
                    task=task,
                    generated_code=generated_code,
                    test_code=test_code,
                    entry_point=entry_point,
                    test_files=test_files,
                    params=params,
                )
            else:
                return self._run_function_completion(
                    run_dir=run_dir,
                    task=task,
                    generated_code=generated_code,
                    test_code=test_code,
                    entry_point=entry_point,
                    params=params,
                )
        except Exception as e:
            logger.exception("Code task %s failed with exception: %s", task_id, e)
            return self._error_result(
                error_message=str(e),
                generated_code=generated_code,
                preserve=not self._preserve_on_success,
                run_dir=run_dir,
            )

    def _extract_code_from_fence(self, code: str) -> str:
        """Extract Python code from markdown fenced code blocks.
        
        Strips ```python ... ``` wrappers. Falls through unchanged
        if no fences detected.
        """
        stripped = code.strip()
        if stripped.startswith("```"):
            lines = stripped.splitlines()
            # Remove first line (fence marker like ```python)
            if lines[0].startswith("```"):
                lines = lines[1:]
            # Remove last line (closing fence)
            if lines and lines[-1].strip().startswith("```"):
                lines = lines[:-1]
            return "\n".join(lines) + "\n"
        # No fences — preserve original code with its trailing newline
        return code

    def _run_function_completion(
        self,
        run_dir: str,
        task: dict[str, Any],
        generated_code: str,
        test_code: str,
        entry_point: str | None,
        params: dict[str, Any],
    ) -> dict[str, Any]:
        """Execute a function-completion code task.

        Writes generated code and test code to the temp directory,
        runs pytest, and returns results.
        """
        task_id = task.get("id", "unknown")
        # Strip markdown code fences if present
        generated_code = self._extract_code_from_fence(generated_code)
        generated_path = os.path.join(run_dir, "generated_code.py")
        test_path = os.path.join(run_dir, "test_generated.py")

        # Write generated code
        with open(generated_path, "w") as f:
            f.write(generated_code)

        code_type = task.get("code_type", "function_completion")
        # Build the test file content
        test_content = self._build_test_file(
            test_code, entry_point, rewrite_imports=(code_type == "function_completion")
        )
        with open(test_path, "w") as f:
            f.write(test_content)

        # Save test output file
        output_path = os.path.join(run_dir, "test_output.txt")

        # Check syntax of generated code
        is_valid, syntax_error = _check_syntax(generated_code)
        if not is_valid:
            logger.warning("Generated code has syntax error: %s", syntax_error)
            test_output = f"SYNTAX ERROR: {syntax_error}\n"
            with open(output_path, "w") as f:
                f.write(test_output)
            return self._syntax_error_result(
                error_message=syntax_error,
                test_output=test_output,
                generated_code=generated_code,
                run_dir=run_dir,
            )

        # Run pytest
        test_output = self._run_pytest(run_dir, test_path)
        with open(output_path, "w") as f:
            f.write(test_output)

        # Parse results
        results = _parse_pytest_xml(test_output)
        passed = results["passed"]
        failed = results["failed"]
        total = results["total"]

        # If total is 0 but we have output, check if tests were collected
        if total == 0:
            collected_match = re.search(
                r"(\d+) test(s)? (?:found|collected|passed|error)", test_output
            )
            if collected_match:
                total = int(collected_match.group(1))

        # Determine code status by checking if import works
        code_status = self._determine_code_status(generated_code)

        # Compute scores
        primary_score = (passed / total) if total > 0 else 0.0
        secondary = {
            "tests_passed": passed,
            "tests_failed": failed,
            "tests_total": total,
            "tests_skipped": results["skipped"],
            "code_compiles": True,
            "code_importable": code_status == "compiled",
            "items": results["items"],
        }

        return {
            "score_primary": primary_score,
            "score_secondary": secondary,
            "score_explanation": f"{passed}/{total} tests passed",
            "scorer_version": "1.0",
            "tests_passed": passed,
            "tests_failed": failed,
            "tests_total": total,
            "test_output": test_output,
            "exit_code": 0,
            "generated_code": generated_code,
            "code_status": code_status,
            "run_dir": run_dir,
        }

    def _run_patch_task(
        self,
        run_dir: str,
        task: dict[str, Any],
        generated_code: str,
        test_code: str,
        entry_point: str | None,
        test_files: list[str],
        params: dict[str, Any],
    ) -> dict[str, Any]:
        """Execute a patch-generation code task.

        Applies the generated patch, verifies it applied correctly,
        then runs tests.
        """
        task_id = task.get("id", "unknown")
        patch_path = os.path.join(run_dir, "patch.diff")
        output_path = os.path.join(run_dir, "test_output.txt")

        # Write the patch file
        with open(patch_path, "w") as f:
            f.write(generated_code)

        # Initialize a git repo for patch application
        self._init_git_repo(run_dir)

        # Try to apply the patch with dry-run first
        apply_result = subprocess.run(
            ["git", "apply", "--check", "patch.diff"],
            cwd=run_dir,
            capture_output=True,
            text=True,
            timeout=30,
        )

        if apply_result.returncode != 0:
            patch_error = apply_result.stderr or apply_result.stdout
            logger.warning("Patch failed to apply: %s", patch_error)
            test_output = f"PATCH APPLY ERROR:\n{patch_error}\n"
            with open(output_path, "w") as f:
                f.write(test_output)
            return self._patch_failed_result(
                error_message=patch_error.strip(),
                test_output=test_output,
                generated_code=generated_code,
                run_dir=run_dir,
            )

        # Apply the patch
        apply_result = subprocess.run(
            ["git", "apply", "patch.diff"],
            cwd=run_dir,
            capture_output=True,
            text=True,
            timeout=30,
        )

        if apply_result.returncode != 0:
            patch_error = apply_result.stderr or apply_result.stdout
            logger.warning("Patch failed to apply (second attempt): %s", patch_error)
            test_output = f"PATCH APPLY ERROR:\n{patch_error}\n"
            with open(output_path, "w") as f:
                f.write(test_output)
            return self._patch_failed_result(
                error_message=patch_error.strip(),
                test_output=test_output,
                generated_code=generated_code,
                run_dir=run_dir,
            )

        # Check for unrelated file changes
        diff_result = subprocess.run(
            ["git", "diff", "--name-only"],
            cwd=run_dir,
            capture_output=True,
            text=True,
            timeout=10,
        )
        if diff_result.returncode == 0:
            modified_files = [
                f for f in diff_result.stdout.strip().splitlines() if f
            ]
            if test_files and set(modified_files) != set(test_files):
                logger.warning(
                    "Unexpected file modifications: %s (expected: %s)",
                    modified_files,
                    test_files,
                )

        # Now run tests on the patched code
        test_path = os.path.join(run_dir, "test_generated.py")
        test_content = self._build_test_file(
            test_code, entry_point, additional_paths=["src"],
            rewrite_imports=False,
        )
        with open(test_path, "w") as f:
            f.write(test_content)

        output_path = os.path.join(run_dir, "test_output.txt")
        test_output = self._run_pytest(run_dir, test_path)
        with open(output_path, "w") as f:
            f.write(test_output)

        results = _parse_pytest_xml(test_output)
        passed = results["passed"]
        failed = results["failed"]
        total = results["total"]

        if total == 0:
            collected_match = re.search(
                r"(\d+) test(s)? (?:found|collected|passed|error)", test_output
            )
            if collected_match:
                total = int(collected_match.group(1))

        primary_score = (passed / total) if total > 0 else 0.0
        secondary = {
            "tests_passed": passed,
            "tests_failed": failed,
            "tests_total": total,
            "tests_skipped": results["skipped"],
            "code_compiles": True,
            "code_importable": True,
            "items": results["items"],
            "code_status": "patch_applied",
        }

        return {
            "score_primary": primary_score,
            "score_secondary": secondary,
            "score_explanation": f"{passed}/{total} tests passed",
            "scorer_version": "1.0",
            "tests_passed": passed,
            "tests_failed": failed,
            "tests_total": total,
            "test_output": test_output,
            "exit_code": 0,
            "generated_code": generated_code,
            "code_status": "patch_applied",
            "run_dir": run_dir,
        }

    def _init_git_repo(self, run_dir: str) -> None:
        """Initialize a git repository in the run directory."""
        subprocess.run(
            ["git", "init", "--initial-branch=main"],
            cwd=run_dir,
            capture_output=True,
            timeout=10,
        )
        subprocess.run(
            ["git", "config", "user.email", "benchmark@local"],
            cwd=run_dir,
            capture_output=True,
            timeout=10,
        )
        subprocess.run(
            ["git", "config", "user.name", "Benchmark"],
            cwd=run_dir,
            capture_output=True,
            timeout=10,
        )

    def _build_test_file(
        self,
        test_code: str,
        entry_point: str | None,
        additional_paths: list[str] | None = None,
        rewrite_imports: bool = True,
    ) -> str:
        """Build a pytest test file from the test_code specification.

        If test_code is empty, generates a basic test that imports the
        module and verifies the entry point exists.

        When rewrite_imports is True (function completion tasks), imports
        like ``from benchmark_000 import func`` are rewritten to
        ``from generated_code import func`` since generated code is
        always written to generated_code.py in the temp directory.
        When False (patch tasks), imports are preserved as-is since
        the patch applies to existing files that are imported by name.
        """
        # Ensure generated_code.py is importable
        sys_header = """import sys
import os
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

"""

        if test_code:
            if rewrite_imports:
                import re
                rewritten = re.sub(
                    r'from\s+[\w_]+\s+import\s+',
                    'from generated_code import ',
                    test_code,
                )
                return sys_header + textwrap.dedent(rewritten)

            # For patch tasks, preserve imports as-is
            return sys_header + textwrap.dedent(test_code)

        # Generate a minimal test file when no test_code is provided
        test_file = sys_header + textwrap.dedent(
            """\
            import generated_code
            assert generated_code is not None, "Module did not import successfully"
            """
        )

        if entry_point:
            test_file += textwrap.dedent(
                f"""\
            def test_entry_point_exists():
                \"\"\"Verify the expected function/class exists.\"\"\"
                assert hasattr(generated_code, "{entry_point}"), (
                    f"Expected entry point '{entry_point}' not found in generated_code"
                )
            """
            )

        return test_file

    def _run_pytest(
        self,
        run_dir: str,
        test_path: str,
        timeout: int = 120,
    ) -> str:
        """Run pytest via subprocess and return stdout+stderr.

        Uses sys.executable to ensure we use the venv's Python.
        """
        try:
            result = subprocess.run(
                [sys.executable, "-m", "pytest", test_path, "-v", "-s", "--tb=long"],
                cwd=run_dir,
                capture_output=True,
                text=True,
                timeout=timeout,
            )
            return result.stdout + result.stderr
        except subprocess.TimeoutExpired:
            return f"TIMEOUT: pytest did not complete within {timeout}s"
        except FileNotFoundError:
            return f"ERROR: pytest not found. sys.executable={sys.executable}"
        except Exception as e:
            return f"ERROR running pytest: {e}"

    def _determine_code_status(self, code: str) -> str:
        """Determine the status of the generated code.

        Returns one of: compiled, syntax_error, import_error, valid_python
        """
        is_valid, _ = _check_syntax(code)
        if not is_valid:
            return "syntax_error"

        is_importable, _ = _attempt_import(code)
        if is_importable:
            return "compiled"

        return "valid_python"

    def _error_result(
        self,
        error_message: str,
        generated_code: str,
        preserve: bool,
        run_dir: str,
    ) -> dict[str, Any]:
        """Create an error result dict."""
        return {
            "score_primary": 0.0,
            "score_secondary": None,
            "score_explanation": f"Runner error: {error_message}",
            "scorer_version": "1.0",
            "tests_passed": 0,
            "tests_failed": 0,
            "tests_total": 0,
            "test_output": f"RUNNER ERROR: {error_message}",
            "exit_code": 1,
            "generated_code": generated_code,
            "code_status": "error",
            "error_message": error_message,
            "run_dir": run_dir if preserve else None,
        }

    def _syntax_error_result(
        self,
        error_message: str,
        test_output: str,
        generated_code: str,
        run_dir: str,
    ) -> dict[str, Any]:
        """Create a syntax error result dict."""
        return {
            "score_primary": 0.0,
            "score_secondary": None,
            "score_explanation": f"Syntax error in generated code: {error_message}",
            "scorer_version": "1.0",
            "tests_passed": 0,
            "tests_failed": 0,
            "tests_total": 0,
            "test_output": test_output,
            "exit_code": 1,
            "generated_code": generated_code,
            "code_status": "syntax_error",
            "error_message": error_message,
            "run_dir": run_dir,
        }

    def _patch_failed_result(
        self,
        error_message: str,
        test_output: str,
        generated_code: str,
        run_dir: str,
    ) -> dict[str, Any]:
        """Create a patch failure result dict."""
        return {
            "score_primary": 0.0,
            "score_secondary": None,
            "score_explanation": f"Patch failed to apply: {error_message}",
            "scorer_version": "1.0",
            "tests_passed": 0,
            "tests_failed": 0,
            "tests_total": 0,
            "test_output": test_output,
            "exit_code": 1,
            "generated_code": generated_code,
            "code_status": "patch_failed",
            "error_message": error_message,
            "run_dir": run_dir,
        }
